	public static void sort(String[][] list) {
		String[][] temp = new String[1][3];
		for (int i = list.length - 1; i > 0; i--) {
			for (int j = 0; j < i; j++) {
				double d1 = new Double(list[j][2]);
				double d2 = new Double(list[j + 1][2]);
				if (d2 > d1) {
					temp[0][0] = list[j][0];
					temp[0][1] = list[j][1];
					temp[0][2] = list[j][2];

					list[j][0] = list[j + 1][0];
					list[j][1] = list[j + 1][1];
					list[j][2] = list[j + 1][2];

					list[j + 1][0] = temp[0][0];
					list[j + 1][1] = temp[0][1];
					list[j + 1][2] = temp[0][2];
				}
			}
		}
	}

