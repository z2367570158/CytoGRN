	public static String readString(InputStream source) throws IOException {
		StringWriter writer = new StringWriter();
		BufferedReader reader = new BufferedReader(new InputStreamReader(source));
		try {
			char[] buffer = new char[BUFFER_SIZE];
			int charactersRead = reader.read(buffer, 0, buffer.length);
			while (charactersRead != -1) {
				writer.write(buffer, 0, charactersRead);
				charactersRead = reader.read(buffer, 0, buffer.length);
			}
		} finally {
			reader.close();
		}
		return writer.toString();
	}

	public static double[][] readDataFile(File f) {
		double[][] result = null;
		try {
			FileInputStream fin = new FileInputStream(f);
			String text = readString(fin);

			String[] lines = text.split("\n");
			if (lines.length == 0) {
				return null;
			}
			String line = lines[0];
			String[] items = line.split("\t");

			result = new double[lines.length][items.length];

			int rowIndex = 0;
			int colIndex = 0;

			for (int i = 0; i < lines.length; i++) {
				for (int j = 0; j < items.length; j++) {
					line = lines[i];
					items = line.split("\t");
					String data = items[j].trim();
					if (!data.equals("")) {
						result[rowIndex][colIndex] = new Double(data);
						colIndex++;
					}
				}
				rowIndex++;
				colIndex = 0;
			}

		} catch (FileNotFoundException e) {
			return Common.data;
		} catch (IOException e) {
		}

		return result;
	}

